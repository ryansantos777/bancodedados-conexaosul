    private void preencherTabela(){
        funcionariosDAO funcionariosDAO = new funcionariosDAO();
        List<funcionarios> listaFuncionarios = funcionariosDAO.getFuncionarios();
        DefaultTableModel tbFuncionarios = (DefaultTableModel) jtFuncionarios.getModel();
        
        for(funcionarios p : listaFuncionarios){
            Object[] obj = new Object[]{
                p.getId(),
                p.getNome(),
                p.getCpf(),
                p.getEmail(),
                p.getTelefone()
            };
            tbFuncionarios.addRow(obj);
            
        }
        
    }
